fn main(){
    //Signed Integer Type
    let x : i32 = -200;
    let y:i32 = 200;


    println!(" X = {}", x);
    println!(" Y = {}", y);

    //Unsigned Integer Type
    let x1 :u32 = 300;
    println!("X  = {}", x1);

    //FLoating Point Values
   // f32 floating point type
   let f1: f32 = 3.1;

   // f64 floating point type
   let f2: f64 = 45.0000031;

   println!("f1 = {}", f1);
   println!("f2 = {}", f2);

   //Boolean value
   let flag1 : bool = true;
   let flag2 : bool =  false;

   println!("Flag = {}", flag1);
   println!("Flag 2 = {}",flag2);

   // char type
   // char type
   let character: char = 'z';
   let special_character: char = '$';

   println!("character = {}", character);
   println!("special_character = {}", special_character);

    //Type Inferene
    let inf = 51;
    println!(" Inference =  {}", inf);

}